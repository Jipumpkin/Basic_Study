package for문;

public class Ex00for문 {

	public static void main(String[] args) {
		
		// 반복문 : 특정 명령을 반복할 수 있도록 도와주는 문법
		// - while, do-while, for
		
		// for
		// : 반복 횟수가 정해져있을때 사용하는 반복문
		
		// *문법구조
		// for(초기화구문; 조건식; 반복 후 작업)
		// for(시작점; 도착점; 보폭)
		
		// 1 2 3 4 5
		for(int i = 1; i < 6; i++) {
			System.out.println(i+" ");
		}
		
		// 1부터 10까지 출력하는 for문을 작성해보자
		
		for(int i = 1; i < 11; i++) {
			System.out.println(i);
		}
		
		
		
		

		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
