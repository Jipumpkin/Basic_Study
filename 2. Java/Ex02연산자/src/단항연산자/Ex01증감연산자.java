package 단항연산자;

public class Ex01증감연산자 {

	public static void main(String[] args) {
		// +=, -- (변수 앞,뒤)
		// num++ ---> num = num+1 (num+=1)
		
		int num = 3;
		
		System.out.println(num++); //3
		// 증감 연산자는 뒤에 붙어 있으면
		// 먼저 실행코드(출력문)이 동작하고나서 연산을 진행한다
		// 콘솔에선 볼 수 없지만 num = 4 인 상태이다.
		System.out.println(--num); //3
		// 증감 연산자는 앞에 붙어 있으면
		// 먼저 연산이 진행되고 나서 실행코드가 동작한다
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
